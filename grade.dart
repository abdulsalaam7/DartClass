void main() {
  num percentage = 83;

  if (percentage >= 90) {
    print("A+");
  } else if (percentage >= 80) {
    print("A");
  } else if (percentage >= 70) {
    print("B");
  } else if (percentage >= 60) {
    print("C");
  } else if (percentage >= 50) {
    print("D");
  } else if (percentage >= 40) {
    print("E");
  } else {
    print("Fail");
  }
}
